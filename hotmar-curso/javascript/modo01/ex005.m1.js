// operador ternario é representado por ?

var idade = 20;

idade >= 18 ? console.log('pode') : console.log('nao pode')

// o que ele ta dizendo aqui...  (idade >= 18 ?) ta perguntando se isso aqui pe verdade, os (console.log()) vai ajuda nessa verificação, o primeiro vai ser afirmando positivo e o segundo vai ser negativo, a separação vai ser feita pelo (:). 

// parecido com o if e else, porem é feito em uma linha só.

// usado para fazer uma verificação simples e deixar dentro de uma variavel, 


var ida = 20;

var pod = ida >= 17 ? true : false; // true (verdade) false(falso)

// deixando a variavel pod aqui, nao precisa do console.log depois do ?.

console.log(pod)

// tbm pode fazer de uma maneira mais simplificda. mais lembrando que (var pod = ida >= 17 ?) depois do ponto de ? tem que ser positivo e depois do : vai ter que ser negativo. o console.log(pod) é so para mostra a variavel.

